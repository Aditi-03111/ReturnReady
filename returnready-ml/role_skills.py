def get_role_skills(target_role: str, top_n: int = 8) -> dict:
    """
    Returns top N skills for a given role with their frequencies.
    Uses fuzzy keyword matching against known roles.
    Falls back to a general tech skill set if no match found.
    """
    role_lower = target_role.lower()

    # Direct keyword match
    for key in ROLE_SKILLS_FREQ:
        if key in role_lower or role_lower in key:
            skills = ROLE_SKILLS_FREQ[key]
            # Return top N by frequency
            return dict(sorted(skills.items(), key=lambda x: x[1], reverse=True)[:top_n])

    # Partial word match
    role_words = set(role_lower.split())
    best_match = None
    best_overlap = 0
    for key in ROLE_SKILLS_FREQ:
        key_words = set(key.split())
        overlap = len(role_words & key_words)
        if overlap > best_overlap:
            best_overlap = overlap
            best_match = key

    if best_match and best_overlap > 0:
        skills = ROLE_SKILLS_FREQ[best_match]
        return dict(sorted(skills.items(), key=lambda x: x[1], reverse=True)[:top_n])

