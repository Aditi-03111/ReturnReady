def get_missing_skills(user_skill_names: list[str], target_role: str, threshold: float = 0.5) -> list[str]:
    """
    Compares user's skills against role frequency table.
    Returns skills that appear in >threshold of job postings but are missing from user profile.
    """
    role_skills = get_role_skills(target_role, top_n=12)
    user_skills_lower = {s.lower() for s in user_skill_names}

    missing = []
    for skill, freq in role_skills.items():
        if freq >= threshold:
            # Check if user has this skill (fuzzy)
            skill_lower = skill.lower()
            has_skill = any(
                skill_lower in u or u in skill_lower
                for u in user_skills_lower
            )
            if not has_skill:
                missing.append(skill)

    return missing
